export function mergeUnitDisplay(unit: UnitData): UnitData {
  const p = PATCH[unit.id];
  if (!p) return unit;
  return {
    ...unit,
    specialAbility: p.specialAbility ?? unit.specialAbility,
    description: p.description ?? unit.description,
    passive: {
      ...unit.passive,
      description: p.passiveDescription ?? unit.passive.description,
    },
  };
}
